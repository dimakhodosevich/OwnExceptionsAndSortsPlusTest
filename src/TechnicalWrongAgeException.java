public class TechnicalWrongAgeException extends Exception{
    public TechnicalWrongAgeException() {
        super();
    }

    public TechnicalWrongAgeException(String message) {
        super(message);
    }

    public TechnicalWrongAgeException(String message, Throwable cause) {
        super(message, cause);
    }

    public TechnicalWrongAgeException(Throwable cause) {
        super(cause);
    }
}
